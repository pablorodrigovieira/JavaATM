    /**
     * Method to Check user information on DB
     * @throws Exceptions.EmptyInputException
     * @throws Exception
     * @return true if found user, false if not
     */
    private boolean databaseUserCheck() throws  Exception, SQLException{        
        try{
            String connectionURL = "jdbc:derby://localhost:1527/BankDatabase";
            Connection conn = DriverManager.getConnection(connectionURL, "bank","bank");
            
            String ID = jTextFieldUserID.getText();
            String PIN = jPasswordField.getText();
          
            String sql = "SELECT * FROM CUSTOMER WHERE ID = "+ID+" AND PIN = "+PIN;
            Helper.Constants.UserID = ID; //Get user ID for use in future
            
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            
            
            //SELECT ALL
            rs.next();
            int row = rs.getRow();
           
            
            if(row > 0)
            {
                rs.close();
                st.close();
                conn.close();
                return true;
            }
            else    
            {
                rs.close();
                st.close();
                conn.close();
                return false;
            }            
                            
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null, ex.toString());
        }
        catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex.toString());
        }
        return false;
    }
