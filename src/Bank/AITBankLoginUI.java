    private void jButtonEnterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEnterActionPerformed
       try{
           checkEmptyFields();
           boolean login = databaseUserCheck();
            
            if(login)
            {
                Helper.displayScreens display = new Helper.displayScreens();
                display.openMainUI(); //open Main Screen
                this.setVisible(false);//close current screen
            }
            else{
                jLabelError.setText(Helper.Constants.LoginError);
            }
       }
       catch(Exceptions.EmptyInputException ex){
           jLabelError.setText(ex.toString());
       }
       catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex.toString());
        }
    }//GEN-LAST:event_jButtonEnterActionPerformed

    private void jPasswordFieldKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jPasswordFieldKeyTyped
        //Check for number only and maximum 4 character
        char vchar = evt.getKeyChar();
        if(!(Character.isDigit(vchar))||
            (vchar == KeyEvent.VK_BACK_SPACE)||
            (vchar == KeyEvent.VK_DELETE)||
            (jPasswordField.getText().length() >= 4))
        {
            evt.consume();
        }
    }//GEN-LAST:event_jPasswordFieldKeyTyped

